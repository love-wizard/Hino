package com.hino.click.extension;

import org.apache.click.control.AbstractContainer;

public class Ul extends AbstractContainer{
	private static final long serialVersionUID = 1065858548854698997L;

	public Ul(String name)
	{
		super(name);
	}

	@Override
	public String getTag() { 
         return "ul"; 
    } 
	
	public void setCss(String cssName)
	{
		setAttribute("class", cssName);
	}
}