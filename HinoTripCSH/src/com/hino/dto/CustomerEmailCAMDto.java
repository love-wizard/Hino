package com.hino.dto;

import java.util.List;

import org.apache.commons.fileupload.FileItem;

import com.hino.model.Staff;

public class CustomerEmailCAMDto {
	private long id;
	private int customerfilter;
	private int groupid;
	private String title;
	private String content;
	private Staff sender;
	private FileItem attachment;
	private List<FileItem> attachements;

	public List<FileItem> getAttachments() {
		return attachements;
	}

	public void setAttachments(List<FileItem> attachements) {
		this.attachements = attachements;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public int getCustomerfilter() {
		return customerfilter;
	}

	public void setCustomerfilter(int customerfilter) {
		this.customerfilter = customerfilter;
	}

	public int getGroupid() {
		return groupid;
	}

	public void setGroupid(int groupid) {
		this.groupid = groupid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Staff getSender() {
		return sender;
	}

	public void setSender(Staff sender) {
		this.sender = sender;
	}

	public FileItem getAttachment() {
		return attachment;
	}

	public void setAttachment(FileItem attachment) {
		this.attachment = attachment;
	}

}
